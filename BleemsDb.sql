CREATE PROCEDURE [dbo].[GetItemDetails] 
	@ProductId INT
AS
BEGIN
	SELECT ProductName, ProductDescription, CONCAT(ProductPrice, Currency) AS Price
    FROM Products
    WHERE ProductId = @ProductId 
