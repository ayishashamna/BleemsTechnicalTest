CREATE VIEW [dbo].[ProductsCategoryView]
AS
SELECT        dbo.Products.ProductId, dbo.Products.ProductName, dbo.Products.ProductDescription, dbo.Products.ProductPrice, dbo.Products.Currency, dbo.Products.PhotoUrl, dbo.Products.Date, dbo.Categories.CategoryId, 
                         dbo.Categories.Name AS CategoryName, dbo.Categories.Description AS CategoryDescription
FROM            dbo.Products INNER JOIN
                         dbo.Categories ON dbo.Products.CategoryId = dbo.Categories.CategoryId
GO
